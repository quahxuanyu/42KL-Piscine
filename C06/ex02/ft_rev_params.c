#include <unistd.h>

void	put_str(char *str)
{
	int	i;

	i = -1;
	while (str[++i] != '\0')
		write(1, &str[i], 1);
}

int	main(int argc, char *argv[])
{
	while (--argc > 0)
	{
		put_str(argv[argc]);
		put_str("\n");
	}
}
